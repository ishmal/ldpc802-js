	getB(code) {
		const pos = code.kb;
		const B = [];
		for (let i = 0, len = code.mb - 1; i < len; i++) {
			const val = code.Hb[i][pos];
			B.push(val);
		}
		return B;
	}
	
	getC(code) {
		const C = code.Hb[code.mb - 1].slice(0, code.kb);
		return C;
	}
	
	getD(code) {
		const D = [code.Hb[code.mb - 1][code.kb]];
		return D;
	}
	
	getE(code) {
		const E = code.Hb[code.mb - 1].slice(code.kb + 1);
		return E;
	}
	
	getT(code) {
		const pos = code.kb + 1;
		const T = [];
		for (let i = 0, len = code.mb - 1; i < len; i++) {
			const row = code.Hb[i];
			T.push(row.slice(pos));
		}
		return T;
	}
		
